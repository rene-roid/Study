// Arithmetic Operators

// + = Add
// - = Subtract
// * = Multiply
// / = Divide
// % = Modulus
let number = 50;

number = number +10; // 60

console.log(number);

// Compound Assignment Operators
number += 10; // 70

console.log(number);


// Incrementing and Decrementing
// number += 1
number++;

console(number++); // Shows the number and then increments
console(++number); // Increments the number and then shows the number